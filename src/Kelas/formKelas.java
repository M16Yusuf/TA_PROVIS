    public void tampilKelas(){
        Object[] row = {"ID Kelas", "Tingkat Kelas","NIP Wali Kelas", "Nama Wali Kelas"}; 
        DefaultTableModel tabmodel = new DefaultTableModel(null,row);
        JTableKelas.setModel(tabmodel); 
        
        sambungDatabase sambung = new sambungDatabase();
        try{
            String sql = "SELECT tb_kelas.id_kelas,tb_kelas.tingkat_kelas,tb_guru.nip,tb_guru.nama_guru FROM tb_guru JOIN tb_kelas ON tb_guru.nip = tb_kelas.nip_walikelas ";
            ResultSet res = sambung.stat.executeQuery(sql);
            while(res.next()){
                String id_kelas = res.getString("id_kelas");
                String tingkat_kelas = String.valueOf(res.getInt("tingkat_kelas"));
                String nip = res.getString("nip");
                String nama_wali = res.getString("nama_guru");
                String[] data = {id_kelas,tingkat_kelas,nip,nama_wali};
                tabmodel.addRow(data);
            }
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
            System.out.println("Koneksi Gagal "+e.toString());
        }   
    }
    
