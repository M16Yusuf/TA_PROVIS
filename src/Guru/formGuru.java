    public void tampilGuru(){
        Object[] row = {"NIP ","Nama Guru","Alamat","Tanggal Lahir"}; 
        DefaultTableModel tabmodel = new DefaultTableModel(null,row);
        JTableGuru.setModel(tabmodel); 
        
        sambungDatabase sambung = new sambungDatabase();
        try{
            String sql = "SELECT * FROM tb_guru";
            ResultSet res = sambung.stat.executeQuery(sql);
            while(res.next()){
                String nip_guru = res.getString("NIP");
                String nama_guru = res.getString("nama_guru");
                String alamat = res.getString("alamat_guru");
                String tgl_lahir = tglview.format(res.getDate("tgl_lahir_guru"));
                String[] data = {nip_guru,nama_guru,alamat,tgl_lahir};
                tabmodel.addRow(data);
            }
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
            System.out.println("Koneksi Gagal "+e.toString());
        }   
    }
    
